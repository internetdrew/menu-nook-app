alter table public.menus
add constraint menus_slug_format_check
check (
  slug is null
  or (
    length(slug) between 3 and 60
    and slug ~ '^[a-z0-9]+(-[a-z0-9]+)*$'
    and slug <> all(array[
      'new',
      'edit',
      'admin',
      'login',
      'signup',
      'settings',
      'pricing',
      'api',
      'www',
      'app',
      'help',
      'support'
    ])
  )
) not valid;

create table if not exists public.menu_slug_redirects (
  id bigint generated by default as identity primary key,
  menu_id uuid not null references public.menus(id) on update cascade on delete cascade,
  slug text not null,
  source text not null default 'manual_change',
  created_at timestamp with time zone not null default now(),
  constraint menu_slug_redirects_slug_key unique (slug),
  constraint menu_slug_redirects_slug_format_check check (
    length(slug) between 3 and 60
    and slug ~ '^[a-z0-9]+(-[a-z0-9]+)*$'
    and slug <> all(array[
      'new',
      'edit',
      'admin',
      'login',
      'signup',
      'settings',
      'pricing',
      'api',
      'www',
      'app',
      'help',
      'support'
    ])
  )
);

create or replace function public.enforce_menu_slug_ref_uniqueness()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  conflicting_id uuid;
begin
  if tg_table_name = 'menus' then
    if new.slug is null then
      return new;
    end if;

    select r.menu_id
    into conflicting_id
    from public.menu_slug_redirects r
    where r.slug = new.slug
      and r.menu_id <> new.id
    limit 1;

    if found then
      raise exception 'That link is already taken.' using errcode = 'P0001';
    end if;

    return new;
  end if;

  select m.id
  into conflicting_id
  from public.menus m
  where m.slug = new.slug
    and m.id <> new.menu_id
  limit 1;

  if found then
    raise exception 'That link is already taken.' using errcode = 'P0001';
  end if;

  return new;
end;
$$;

drop trigger if exists enforce_menus_slug_ref_uniqueness on public.menus;
create trigger enforce_menus_slug_ref_uniqueness
before insert or update of slug on public.menus
for each row
execute function public.enforce_menu_slug_ref_uniqueness();

