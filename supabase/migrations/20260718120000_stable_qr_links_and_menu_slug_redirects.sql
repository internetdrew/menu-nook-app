create or replace function public.update_menu_settings(
  p_menu_id uuid,
  p_name text,
  p_slug text
)
returns public.menus
language plpgsql
security definer
set search_path = public
as $$
declare
  current_menu public.menus%rowtype;
  updated_menu public.menus%rowtype;
  normalized_slug text := trim(p_slug);
  conflicting_menu_id uuid;
begin
  select m.*
  into current_menu
  from public.menus m
  join public.businesses b on b.id = m.business_id
  where m.id = p_menu_id
    and b.user_id = auth.uid()
  for update;

  if not found then
    raise exception 'Menu not found.' using errcode = 'P0001';
  end if;

  select m.id
  into conflicting_menu_id
  from public.menus m
  where m.slug = normalized_slug
    and m.id <> p_menu_id
  limit 1;

  if found then
    raise exception 'That link is already taken.' using errcode = 'P0001';
  end if;

  select r.menu_id
  into conflicting_menu_id
  from public.menu_slug_redirects r
  where r.slug = normalized_slug
    and r.menu_id <> p_menu_id
  limit 1;

  if found then
    raise exception 'That link is already taken.' using errcode = 'P0001';
  end if;

  if current_menu.slug is not null and current_menu.slug <> normalized_slug then
    insert into public.menu_slug_redirects (menu_id, slug, source)
    values (p_menu_id, current_menu.slug, 'manual_change')
    on conflict (slug) do nothing;
  end if;

  delete from public.menu_slug_redirects
  where menu_id = p_menu_id
    and slug = normalized_slug;

  update public.menus
  set name = p_name,
      slug = normalized_slug
  where id = p_menu_id
  returning * into updated_menu;

  return updated_menu;
end;
$$;

grant execute on function public.update_menu_settings(uuid, text, text) to authenticated;
grant execute on function public.update_menu_settings(uuid, text, text) to service_role;
